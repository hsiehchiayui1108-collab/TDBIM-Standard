import yaml

def generate_markdown():
    # 讀取原始 YAML 檔案
    with open('規範.yaml', 'r', encoding='utf-8') as f:
        data = yaml.safe_load(f)
    
    meta = data['架構元定義']
    
    markdown_content = f"""# 專案軀體圖編製規範

本規範定義軀體圖（涵蓋建築、結構、裝修、機電）的產出標準，服務模板、鋼筋、混凝土三大工項。

---

## 設計哲學與仲裁原則
"""
    # 寫入設計哲學
    for p in data['設計哲學']['原則']:
        role = f"【{p['角色']}】" if '角色' in p else ""
        markdown_content += f"* **{p['名稱']}{role}：** {p['說明'].strip()}\n"
        
    markdown_content += """
---

## 視圖雙維度結構
視圖由「視圖類型」與「資訊範圍」兩個獨立維度正交組合（索引視圖、資訊區塊、表格不套用此矩陣）：

| 資訊範圍 → <br> 視圖類型 ↓ | 整合 (跨專業界面) | 軀體 (僅含混凝土本體) |
| :--- | :--- | :--- |
| **平面視圖** | 整合平面圖 | 軀體見上圖、軀體見下圖 |
| **剖面視圖** | 整合剖面圖、坡段斜率展開圖 | 軀體剖面圖 |
| **3D 視圖** | 3D 整合圖 | 3D 軀體圖 |

---

## 元素類別與屬性規範
| 元素類別 | 定義與判別問題 | 強制屬性 / 可選屬性 | 用例 |
| :--- | :--- | :--- | :--- |
"""
    # 寫入元素類別
    for name, attr in meta['元素類別枚舉'].items():
        req = ", ".join(attr['屬性 schema']['強制'])
        opt = ", ".join(attr['屬性 schema'].get('可選', ['無']))
        markdown_content += f"| **{name}** | {attr['定義']}<br>*{attr['判別問題']}* | 強制：{req}<br>可選：{opt} | {attr['用例']} |\n"

    markdown_content += """
---

## 目的描述撰寫範本（三段結構）
任何層級的目的描述均須依循以下格式填空：

* **圖紙：** 當 `[使用情境]` 時，提供 `[核心內容]`，使 `[角色]` 能 `[使用成果]`。
* **視圖：** 針對「`[使用情境]`」，以 `[核心內容]`，使 `[角色]` 能 `[使用成果]`。
* **元素（必要）：** 呈現 `[核心內容]`，使 `[角色]` 能 `[使用成果]`。
* **元素（視條件）：** 當 `[使用情境]` 時，呈現 `[核心內容]`，使 `[角色]` 能 `[使用成果]`。

---

## 命名硬性規則對照表
名稱必須使用連貫自然語言，嚴禁承擔上下文分類責任。

| ❌ 錯誤示範（禁止） | ⭕ 正確示範（推薦） | 違反規則 |
| :--- | :--- | :--- |
| `牆面開口_門窗` | `門窗牆面開口` | 禁止使用下底線等分隔符。應採「[修飾語][類別詞]」。 |
| `（同上）` | `具體元素名稱` | 禁止無語意的省略寫法。 |
| `B1_平面圖` | `平面圖` | 名稱不應包含結構上下文（如樓層）。 |
"""

    # 寫入產出的 README.md
    with open('README.md', 'w', encoding='utf-8') as f:
        f.write(markdown_content)

if __name__ == '__main__':
    generate_markdown()
